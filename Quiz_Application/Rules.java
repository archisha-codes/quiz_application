        setLayout(null);
        
        JLabel heading = new JLabel("Welcome " + name + " to Question street");
        heading.setBounds(50, 20, 700, 30);
        heading.setFont(new Font("Viner Hand ITC", Font.BOLD, 28));
        heading.setForeground(new Color(30, 144, 254));
        add(heading);
        
        JLabel rules = new JLabel();
        rules.setBounds(20, 90, 700, 350);
        rules.setFont(new Font("Tahoma", Font.PLAIN, 16));
        rules.setText(
            "<html>"+ 
                "1. No other electronics allowed" + "<br><br>" +
                "2. Clear environment: the surface where your device is placed must be entirely clear of anything other than the test device. Books, papers, electronics, and any other objects are not allowed" + "<br><br>" +
                "3. Take the exam independently" + "<br><br>" +
                "4. No communication: no communicating with other individuals, whether verbal, non-verbal, or electronic." + "<br><br>" +
                "5. Well-lit environment, clear webcam view" + "<br><br>" +
                "6. Stay on camera: remain fully visible on the webcam throughout the test" + "<br><br>" +
                "7. One screen only: only one screen is permitted during the test; using any other screens, monitors, or display devices is strictly prohibited" + "<br><br>"  +
            "<html>"
        );
        add(rules);
        
        back = new JButton("Back");
        back.setBounds(250, 500, 100, 30);
        back.setBackground(new Color(30, 144, 254));
        back.setForeground(Color.WHITE);
        back.addActionListener(this);
        add(back);
        
